namespace Tazor.Components.Theming;

/// <summary>
/// Provides theming for the list view component
/// </summary>
public class ListViewTheme
{
    /// <summary>
    /// Gets or Sets the wrapper class for the list view component
    /// </summary>
    public string Wrapper { get; set; } = string.Empty;

    /// <summary>
    /// Gets or Sets the list class for the list view component
    /// </summary>
    public string List { get; set; } = string.Empty;

    /// <summary>
    /// Gets or Sets the list item class for the list view component
    /// </summary>
    public string ListItem { get; set; } = string.Empty;
}
