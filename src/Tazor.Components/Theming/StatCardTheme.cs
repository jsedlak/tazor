namespace Tazor.Components.Theming;

/// <summary>
/// Provides theming for the stat card component
/// </summary>
public class StatCardTheme
{
    /// <summary>
    /// Gets or Sets theming for the card
    /// </summary>
    public string Card { get; set; } = string.Empty;

    /// <summary>
    /// Gets or Sets theming for the header
    /// </summary>
    public string Header { get; set; } = string.Empty;

    /// <summary>
    /// Gets or Sets theming for the title
    /// </summary>
    public string Title { get; set; } = string.Empty;

    /// <summary>
    /// Gets or Sets theming for the additional data
    /// </summary>
    public string AdditionalData { get; set; } = string.Empty;

    /// <summary>
    /// Gets or Sets theming for the body container
    /// </summary>
    public string Body { get;set; } = string.Empty;
    
    /// <summary>
    /// Gets or Sets theming for the statistic
    /// </summary>
    public string Statistic { get; set; } = string.Empty;

    /// <summary>
    /// Gets or Sets theming for the footer container
    /// </summary>
    public string Footer { get; set; } = string.Empty;

    /// <summary>
    /// Gets or Sets theming for the note within the footer
    /// </summary>
    public string Note { get; set; } = string.Empty;
}