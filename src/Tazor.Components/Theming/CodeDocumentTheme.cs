namespace Tazor.Components.Theming;

/// <summary>
/// Provides theming for the CodeBlock component
/// </summary>
public class CodeBlockTheme
{
    /// <summary>
    /// Gets or Sets the wrapper class
    /// </summary>
    public string Wrapper { get; set; } = string.Empty;

    /// <summary>
    /// Gets or Sets the header class
    /// </summary>
    public string Header { get; set; } = string.Empty;

    /// <summary>
    /// Gets or Sets the code block class
    /// </summary>
    public string Pre { get; set; } = string.Empty;
}
